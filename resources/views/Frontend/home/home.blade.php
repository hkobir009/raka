                <p class="card-text text-start">Suchitra Hajong is the co-founder of the Bangladesh Bondhu Foundation (BONDHU). She was born in a Hajong family of Lengura village of Kalmakanda Upazila under Netrokona District. She was the youngest among her 10 siblings. She studied at Lengura Govt. Primary School, Lengura High School, and Durgapur Women’s College. She completed her BSc. in Computer Science from BUBT, Dhaka. She was involved in Bangladesh Indigenous Peoples Forum (BIPF). With the inspiration and assistance of Suchitra Hajong, SZ Consultancy Services Ltd. was established in 2008. In 2015, Bangladesh Bondhu Foundation was founded. From then on, she dedicated herself to make Bondhu Chula (Cook Stove) project a success. She coordinated Bondhu Chula activities of 250 NGOs, 6000 entrepreneurs and nearly 1500 staffs. She was diagnosed with breast cancer in May 2017. In December 2017, she launched a new project titled “Cancer and Women’s Health” to make people aware of cancer. After fighting cancer, she breathed her last on 17th September 2018.</p>
